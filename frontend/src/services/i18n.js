/**
 * Get a translation string.
 * @param {string} key - The translation key
 * @param {string} [langCode] - 2-letter language code (defaults to localStorage)
 * @returns {string}
 */
export function t(key, langCode) {
  const lang = langCode || localStorage.getItem('sv_lang') || 'en';
  return translations[lang]?.[key] || translations.en[key] || key;
}

/**
 * Get all translations for current language.
 */
export function getStrings(langCode) {
  const lang = langCode || localStorage.getItem('sv_lang') || 'en';
  return { ...translations.en, ...translations[lang] };
}

